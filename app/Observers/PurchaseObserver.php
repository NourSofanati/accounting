     * Handle the Purchase "created" event.
     *
     * @param  \App\Models\Purchase  $purchase
     * @return void
     */
    public function created(Purchase $purchase)
    {
        $purchaseTransaction = Transaction::create(
            [
                'transaction_name' => 'فاتورة شراء ' . now(),
                'transaction_date' => $purchase->date,
                'description' => $purchase->notes,
                'currency_id' => 1,
                'currency_value' => $purchase->currency_value,
            ]
        );
        $purchase->update(['transaction_id' => $purchaseTransaction->id]);
    }

